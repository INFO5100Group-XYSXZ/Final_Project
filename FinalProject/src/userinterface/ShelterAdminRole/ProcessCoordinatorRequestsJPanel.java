    public void popTable() {
        DefaultTableModel model = (DefaultTableModel) tblRequest.getModel();
        model.setRowCount(0);
        //System.out.println(userAccount.getWorkQueue().getWorkRequestList().size()); // test
        for (WorkRequest wr : userAccount.getWorkQueue().getWorkRequestList() ) {
           //System.out.println("in workrequest"); //test
           if (wr instanceof AnimalRecord) {
                //System.out.println("in animalrecord"); //test
                Object row[] = new Object[7]; 
                row[0] = wr;
                row[1] = ((AnimalRecord) wr).getReportingRequest().getAnimalType();
                row[2] = ((AnimalRecord) wr).getBreed() ==null ? "--": ((AnimalRecord) wr).getBreed();
                row[3] = ((AnimalRecord) wr).getAge() ==null ? "--": ((AnimalRecord) wr).getAge();
                row[4] = ((AnimalRecord) wr).getShelterRequest().getAssignedStaff() ==null ? "--": ((AnimalRecord) wr).getShelterRequest().getAssignedStaff();
                row[5] = ((AnimalRecord) wr).getShelterRequest().getStatus() ==null ? "--": ((AnimalRecord) wr).getShelterRequest().getStatus();
                row[6] = ((AnimalRecord) wr).getShelterRequest().getLatestMessage();
                       
                ((DefaultTableModel) tblRequest.getModel()).addRow(row);
           }
           
        }       
   
    }
    
    public void popCombo() {
        comboStaff.removeAllItems();
        for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
            if (ua.getRole().toString().equalsIgnoreCase("ShelterStaff") ) {
                comboStaff.addItem(ua);
            }
        } 
    }
    
    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:'
        int row = tblRequest.getSelectedRow();
        if(row<0) {
             JOptionPane.showMessageDialog(null, "Please select a case from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        AnimalRecord ar = (AnimalRecord)tblRequest.getValueAt(row, 0);
        
        if (ar.getShelterRequest().getStatus().equals("Staff Assigned")) {
             JOptionPane.showMessageDialog(null, "Already assigned.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!ar.getShelterRequest().getStatus().equals("Shelter Accepted") ) {
            JOptionPane.showMessageDialog(null, "Please accept before assign to staff", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        UserAccount ua = (UserAccount)comboStaff.getSelectedItem();
        if (ua == null) {
            JOptionPane.showMessageDialog(null, "Please select a Staff to assign", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        ar.getShelterRequest().setLatestMessage(txtMessage.getText());
        String message = "Shelter admin assign staff: " + txtMessage.getText();
        ar.addMessage(message);
        ar.getShelterRequest().setAssignedStaff(ua);
        ar.getShelterRequest().setStatus("Staff Assigned");
        ua.getWorkQueue().getWorkRequestList().add(ar); // add animalrecord to staff's work list
        txtMessage.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Staff assigned");
        popTable();        
    }//GEN-LAST:event_btnAssignActionPerformed

